package com.example.olio_ohjelmointi_ht;

public class User {
    String name;
    int age;
    String password;
    String username;
    String email;
    String city;

    public User(){

    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

    public String getEmail() {
        return email;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getUsername() {
        return username;
    }
}
